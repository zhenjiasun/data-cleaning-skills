#!/usr/bin/env bash
# Eval runner that uses the Claude Code CLI (no API key needed).
#
# Uses your existing Claude Code OAuth login. Each invocation runs with:
#   --tools ""              : pure model response, no tool execution
#   --setting-sources ""    : ignore user / project / local settings (hooks, etc.)
#   --system-prompt …       : either the baseline system prompt or the SKILL.md body
#   workdir = a fresh temp dir, so no CLAUDE.md / auto-memory is auto-loaded
#
# The point is a clean, reproducible comparison — no project context bleeding in.
#
# Usage:
#   ./run.sh                     # all 5 cases, both variants
#   ./run.sh 03                  # one case (matches case slug prefix)
#   MODEL=sonnet ./run.sh        # different model alias
#   SAMPLES=3 ./run.sh           # k samples per variant (concatenated)
#
# Compatible with bash 3.2 (the default on macOS).

set -eu

EVALS_DIR="$(cd "$(dirname "$0")" && pwd)"
SKILLS_DIR="$(cd "$EVALS_DIR/.." && pwd)/skills"
MODEL="${MODEL:-opus}"
SAMPLES="${SAMPLES:-1}"
BASELINE_SYSTEM="You are a helpful data assistant."
FILTER="${1:-}"

CASES="
01-profile-dataset|data-quality/profile-dataset
02-clean-datetime-fields|data-cleaning/clean-datetime-fields
03-validate-join|data-modeling/validate-join
04-detect-data-leakage|machine-learning-data/detect-data-leakage
05-resolve-entities|data-cleaning/resolve-entities
"

# Strip YAML frontmatter from SKILL.md so only the body is the system prompt.
strip_frontmatter() {
  awk 'BEGIN{f=0} /^---$/{f++; next} f>=2 || f==0' "$1"
}

run_cell() {
  case_dir="$1"; variant="$2"; system="$3"; skill_path="$4"
  task="$(cat "$case_dir/task.md")"
  out_file="$case_dir/results/$variant.md"
  mkdir -p "$case_dir/results"

  if [ "$variant" = "baseline" ]; then
    header="# Baseline response (no skill)

> Generated by: claude --model $MODEL, samples=$SAMPLES

"
  else
    header="# Skilled response ($skill_path/SKILL.md)

> Generated by: claude --model $MODEL, samples=$SAMPLES

"
  fi
  printf '%s' "$header" > "$out_file"

  i=1
  while [ "$i" -le "$SAMPLES" ]; do
    [ "$SAMPLES" -gt 1 ] && printf '\n### Sample %d\n\n' "$i" >> "$out_file"
    sample_dir="$(mktemp -d)"
    (
      cd "$sample_dir"
      claude -p \
        --model "$MODEL" \
        --tools "" \
        --setting-sources "" \
        --system-prompt "$system" \
        "$task" < /dev/null
    ) >> "$out_file"
    rm -rf "$sample_dir"
    if [ "$SAMPLES" -gt 1 ] && [ "$i" -lt "$SAMPLES" ]; then
      printf '\n\n---\n' >> "$out_file"
    fi
    i=$((i + 1))
  done
  printf '\n' >> "$out_file"
  echo "  $variant -> $out_file"
}

echo "$CASES" | grep -v '^$' | while IFS='|' read -r case_slug skill_path; do
  if [ -n "$FILTER" ]; then
    case "$case_slug" in
      "$FILTER"*) ;;
      *) continue ;;
    esac
  fi
  case_dir="$EVALS_DIR/cases/$case_slug"
  skill_body="$(strip_frontmatter "$SKILLS_DIR/$skill_path/SKILL.md")"

  echo
  echo "$case_slug"
  run_cell "$case_dir" "baseline" "$BASELINE_SYSTEM" "$skill_path"
  run_cell "$case_dir" "skilled"  "$skill_body"      "$skill_path"
done
