"""
Eval runner: regenerates results/{baseline,skilled}.md for each case.

Usage:
    pip install anthropic
    export ANTHROPIC_API_KEY=...
    python run.py                    # run all cases
    python run.py --case 03          # run one case
    python run.py --samples 5        # k samples per variant for variance analysis

The runner only generates model outputs. Grading (results/grade.md) stays human.
"""
from __future__ import annotations

import argparse
import os
import re
import sys
from pathlib import Path

try:
    from anthropic import Anthropic
except ImportError:
    sys.exit("pip install anthropic")

EVALS_DIR = Path(__file__).resolve().parent
SKILLS_DIR = EVALS_DIR.parent / "skills"
MODEL = os.environ.get("EVAL_MODEL", "claude-opus-4-7")

SKILL_BY_CASE = {
    "01-profile-dataset":        "data-quality/profile-dataset",
    "02-clean-datetime-fields":  "data-cleaning/clean-datetime-fields",
    "03-validate-join":          "data-modeling/validate-join",
    "04-detect-data-leakage":    "machine-learning-data/detect-data-leakage",
    "05-resolve-entities":       "data-cleaning/resolve-entities",
}

BASELINE_SYSTEM = "You are a helpful data assistant."


def read_skill(path: str) -> str:
    """Strip YAML frontmatter and return the body."""
    text = (SKILLS_DIR / path / "SKILL.md").read_text()
    return re.sub(r"^---\n.*?\n---\n", "", text, count=1, flags=re.S).strip()


def run_case(client: Anthropic, case_dir: Path, samples: int) -> None:
    skill_path = SKILL_BY_CASE[case_dir.name]
    user_msg = (case_dir / "task.md").read_text()
    skilled_system = read_skill(skill_path)
    results_dir = case_dir / "results"
    results_dir.mkdir(exist_ok=True)

    for variant, system in [("baseline", BASELINE_SYSTEM), ("skilled", skilled_system)]:
        outputs = []
        for i in range(samples):
            resp = client.messages.create(
                model=MODEL,
                max_tokens=4096,
                system=system,
                messages=[{"role": "user", "content": user_msg}],
            )
            outputs.append(resp.content[0].text)
        body = outputs[0] if samples == 1 else "\n\n---\n\n".join(
            f"### Sample {i+1}\n\n{o}" for i, o in enumerate(outputs)
        )
        header = (
            f"# {variant.capitalize()} response"
            f"{' (no skill)' if variant == 'baseline' else f' ({skill_path}/SKILL.md)'}\n\n"
            f"> Generated by: {MODEL}, samples={samples}\n\n"
        )
        (results_dir / f"{variant}.md").write_text(header + body)
        print(f"  {variant}: {samples} sample(s) → {results_dir / f'{variant}.md'}")


def main() -> None:
    parser = argparse.ArgumentParser()
    parser.add_argument("--case", help="single case slug, e.g. 03 or 03-validate-join")
    parser.add_argument("--samples", type=int, default=1)
    args = parser.parse_args()

    client = Anthropic()
    cases_dir = EVALS_DIR / "cases"
    cases = sorted(p for p in cases_dir.iterdir() if p.is_dir())
    if args.case:
        cases = [p for p in cases if p.name.startswith(args.case)]
        if not cases:
            sys.exit(f"no case matching {args.case!r}")

    for c in cases:
        print(f"\n{c.name}")
        run_case(client, c, args.samples)


if __name__ == "__main__":
    main()
